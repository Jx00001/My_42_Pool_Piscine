#include <unistd.h>

void	ft_is_negative(int n)
{
	char	nv;
	char	pv;

	nv = 'N';
	pv = 'P';
	if (n >= 0)
	{
		write(1, &pv, 1);
	}
	else
	{
		write(1, &nv, 1);
	}
}
