int	ft_atoi(char *str)
{
	int	i;
	int	nbr;

	i = 1;
	nbr = 0;
	while (((*str >= 9 && *str <= 13) || (*str == ' ')))
	{
		str++;
	}
	while ((*str == '-' || *str == '+') && (*str != '\0'))
	{
		if (*str == '-')
			i = i * (-1);
		str++;
	}
	while (*str != '\0' && (*str >= '0' && *str <= '9'))
	{
		nbr = (nbr * 10);
		nbr = nbr + (*str - 48);
		str++;
	}
	nbr = nbr * i;
	return (nbr);
}
